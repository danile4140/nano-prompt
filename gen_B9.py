# ── 主函数 ────────────────────────────────────────────────────────────────────

def build_prompt(item_id: str) -> str:
    sections = PROMPTS[item_id]
    anchor = RATIO_ANCHORS[item_id]
    fingerprint = SCENE_FINGERPRINTS[item_id]
    text = sections.format(
        style=STYLE_SETTING,
        c175=CONSTRAINT_17_5,
        anchor=anchor,
        fingerprint=fingerprint,
        no_text=NO_TEXT_LINE,
    )
    return text


def main():
    src = r"E:\coze-ai-nanobanana\draft_B9.json"
    dst = r"E:\coze-ai-nanobanana\assets_B9_part1.json"

    with open(src, "r", encoding="utf-8") as f:
        data = json.load(f)

    target_ids = {f"B9-{i}" for i in range(1, 11)}
    result = []

    for item in data["storyboard_draft"]:
        if item["id"] not in target_ids:
            continue
        out = {
            "id": item["id"],
            "character_refs": item["character_refs"],
            "scene_refs": item["scene_refs"],
            "covered_sentences": item["covered_sentences"],
            "layout": item["layout"],
            "prompt": build_prompt(item["id"]),
        }
        result.append(out)

    # 按 B9-1..B9-10 顺序排列
    result.sort(key=lambda x: int(x["id"].split("-")[1]))

    output = {"storyboard_assets": result}
    with open(dst, "w", encoding="utf-8", newline="\n") as f:
        f.write(json.dumps(output, ensure_ascii=False, indent=2))

    print(f"写入完成：{dst}，共 {len(result)} 条")
    for item in result:
        preview = item["prompt"][:60].replace("\n", " ")
        print(f"  {item['id']}: {preview}…")


if __name__ == "__main__":
    main()
