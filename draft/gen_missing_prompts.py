def build_assets(draft_file, prompts_dict):
    data = json.load(open(draft_file, encoding='utf-8'))
    assets = []
    for item in data['storyboard_draft']:
        entry = dict(item)
        if item['id'] not in prompts_dict:
            print(f"WARNING: missing prompt for {item['id']}")
        entry['prompt'] = prompts_dict.get(item['id'], "")
        assets.append(entry)
    return {'storyboard_assets': assets}

